  /// Get state data by full name
  static StateData? getStateData(String stateName) {
    return _data[stateName];
  }

  /// Get state data by abbreviation
  static StateData? getStateDataByAbbr(String abbr) {
    return _data.values.firstWhere(
      (state) => state.abbreviation == abbr.toUpperCase(),
      orElse: () => throw StateError('State not found: $abbr'),
    );
  }

  /// Get all states
  static List<StateData> getAllStates() {
    return _data.values.toList()..sort((a, b) => a.name.compareTo(b.name));
  }
